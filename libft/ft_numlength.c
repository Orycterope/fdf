#include "libft.h"

int					ft_numlength(int n)
{
	int		c;

	c = 1;
	if (n == 0)
		return (0);
	if (n < 0)
	{
		n = -n;
		c++;
	}
	while ((n /= 10) > 0)
		c++;
	return (c);
}
